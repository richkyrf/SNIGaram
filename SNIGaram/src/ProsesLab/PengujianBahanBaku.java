    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdPengujianBahanBaku` as 'ID', DATE_FORMAT(`Tanggal`,'%d-%m-%Y') as 'Tanggal', `KadarAir`, `KadarNaCl`, `Warna`, `Penguji`, `Keterangan` FROM `snitbpengujianbahanbaku` WHERE `IdPengujianBahanBaku` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JDTanggal.setDate(FDateF.strtodate(list.get(1), "dd-MM-yyyy"));
        JTKadarAir.setText(list.get(2).split("\\.")[0]);
        JTKomaKadarAir.setText(list.get(2).split("\\.")[1]);
        JTKadarNaCl.setText(list.get(3).split("\\.")[0]);
        JTKomaKadarNaCl.setText(list.get(3).split("\\.")[1]);
        JTWarna.setText(list.get(4));
        JCPenguji.setSelectedItem(list.get(5));
        JTAKeterangan.setText(list.get(6));
    }

    Boolean checkInput() {
        if (JDTanggal.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Tanggal Terima Tidak Boleh Kosong");
            return false;
        } else if (JTKadarAir.getInt() == 0 && JTKomaKadarAir.getInt() == 0) {
            JOptionPane.showMessageDialog(null, "Kadar Air Tidak Boleh Kosong");
            return false;
        } else if (JTKadarNaCl.getInt() == 0 && JTKomaKadarNaCl.getInt() == 0) {
            JOptionPane.showMessageDialog(null, "Kadar NaCl Tidak Boleh Kosong");
            return false;
        } else if (JTWarna.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Warna Tidak Boleh Kosong");
            return false;
        } else {
            return true;
        }
    }

