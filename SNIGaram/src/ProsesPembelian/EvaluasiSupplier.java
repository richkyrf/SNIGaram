    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdEvaluasi` as 'ID', `NamaPemasok` as 'Nama Pemasok', `HasilEvaluasi` as 'Hasil Evaluasi', `Keterangan` FROM `snitbevaluasisupplier` WHERE `IdEvaluasi` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JCNamaPemasok.setSelectedItem(list.get(1));
        jNumberOnly1.setText(list.get(2).split("\\,")[0]);
        jNumberOnly2.setText(list.get(2).split("\\,")[1]);
        jNumberOnly3.setText(list.get(2).split("\\,")[2]);
        jNumberOnly4.setText(list.get(2).split("\\,")[3]);
        jPlaceHolder1.setText(list.get(3).split("\\~")[0]);
        jPlaceHolder2.setText(list.get(3).split("\\~")[1]);
        jPlaceHolder3.setText(list.get(3).split("\\~")[2]);
        jPlaceHolder4.setText(list.get(3).split("\\~")[3]);
    }

    Boolean checkInput() {
        if (JCNamaPemasok.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Nama Pemasok");
            return false;
        } else {
            return true;
        }
    }

    String getHasilEvaluasi() {
        return jNumberOnly1.getText() + "," + jNumberOnly2.getText() + "," + jNumberOnly3.getText() + "," + jNumberOnly4.getText();
    }

    String getKeterangan() {
        return jPlaceHolder1.getText() + "~" + jPlaceHolder2.getText() + "~" + jPlaceHolder3.getText() + "~" + jPlaceHolder4.getText();
    }

