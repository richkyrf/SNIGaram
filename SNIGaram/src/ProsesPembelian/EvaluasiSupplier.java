    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil = false;
            berhasil = insert.simpan("INSERT INTO `snitbevaluasisupplier`(`NamaPemasok`, `HasilEvaluasi`, `Keterangan`) VALUES ('" + JCNamaPemasok.getSelectedItem() + "','" + getHasilEvaluasi() + "','" + getKeterangan() + "')", "Evaluasi Supplier", this);
            if (berhasil) {
                if (listEvaluasiSupplier != null) {
                    listEvaluasiSupplier.load();
                }
                if (tutup) {
                    dispose();
                } else {
                    JCNamaPemasok.setSelectedIndex(0);
                    jNumberOnly1.setText("0");
                    jNumberOnly2.setText("0");
                    jNumberOnly3.setText("0");
                    jNumberOnly4.setText("0");
                    jPlaceHolder1.setText("");
                    jPlaceHolder2.setText("");
                    jPlaceHolder3.setText("");
                    jPlaceHolder4.setText("");
                }
            }
        }
    }

