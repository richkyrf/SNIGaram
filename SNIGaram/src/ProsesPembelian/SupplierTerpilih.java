    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdSupplierTerpilih` as 'ID', `NamaPemasok` as 'Nama Pemasok', `Produk`, `Spesifikasi` FROM `snitbsupplierterplilih` WHERE `IdSupplierTerpilih` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JCNamaPemasok.setSelectedItem(list.get(1));
        JTProduk.setText(list.get(2));
        JTASpesifikasi.setText(list.get(3));
    }

    Boolean checkInput() {
        if (JCNamaPemasok.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Nama Pemasok");
            return false;
        } else if (JTProduk.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Silahkan Isi Nama Produk");
            return false;
        } else {
            return true;
        }
    }

