    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil;
            berhasil = insert.simpan("INSERT INTO `snitbsupplierterplilih`(`NamaPemasok`, `Produk`, `Spesifikasi`) VALUES ('" + JCNamaPemasok.getSelectedItem() + "','" + JTProduk.getText() + "','" + JTASpesifikasi.getText() + "')", "Evaluasi Supplier", this);
            if (berhasil) {
                if (listEvaluasiSupplier != null) {
                    listEvaluasiSupplier.load();
                }
                if (tutup) {
                    dispose();
                } else {
                    JCNamaPemasok.setSelectedIndex(0);
                    JTProduk.setText("");
                    JTASpesifikasi.setText("");
                }
            }
        }
    }

    void ubahData() {
        if (checkInput()) {
            Update update = new Update();
            Boolean berhasil;
            berhasil = update.Ubah("UPDATE `snitbsupplierterplilih` SET `NamaPemasok`='" + JCNamaPemasok.getSelectedItem() + "',`Produk`='" + JTProduk.getText() + "',`Spesifikasi`='" + JTASpesifikasi.getText() + "' WHERE `IdSupplierTerpilih` = " + IdEdit, "Supplier Terpilih", this);
            if (listEvaluasiSupplier != null) {
                listEvaluasiSupplier.load();
            }
            if (berhasil) {
                dispose();
            }
        }
    }

