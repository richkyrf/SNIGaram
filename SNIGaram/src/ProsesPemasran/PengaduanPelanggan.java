    void tambahData(Boolean tutup) {
        if (checkInput()) {
            Insert insert = new Insert();
            Boolean berhasil = false;
            berhasil = insert.simpan("INSERT INTO `snitbpengaduanpelanggan`(`Tanggal`, `AsalInstitusi`, `YangMenerima`, `JenisPengaduan`, `BagianYangMenangani`, `RincianPengaduan`, `TindakLanjutPengaduan`, `TindakanPencegahan`, `TargetTanggalPenyelesaian`, `VerifikasiPenyelesaian`, `Selesai`) VALUES ('" + FDateF.datetostr(JDTanggalPengaduan.getDate(), "yyyy-MM-dd") + "','" + JTAsalInstitusi.getText() + "','" + JTYangMenerima.getText() + "','" + JCJenisPengaduan.getSelectedItem() + "','" + JCBagianYangMenangani.getSelectedItem() + "','" + JTARincianPengaduan.getText() + "','" + JTATindakLanjut.getText() + "','" + JTATindakanPencegahan.getText() + "','" + FDateF.datetostr(JDTanggalTargetPenyelesaian.getDate(), "yyyy-MM-dd") + "','" + JTVerifikasiPenyelesaian.getText() + "'," + JCBSelesai.isSelected() + ")", "Pengaduan Pelanggan", this);
            if (berhasil) {
                if (listPengaduanPelanggan != null) {
                    listPengaduanPelanggan.load();
                }
                if (tutup) {
                    tambahPengaduanPelanggan.dispose();
                    tambahPengaduanPelanggan = null;
                } else {
                    JTAsalInstitusi.setText("");
                    JTYangMenerima.setText("");
                    JTARincianPengaduan.setText("");
                    JTATindakLanjut.setText("");
                    JTATindakanPencegahan.setText("");
                    JTVerifikasiPenyelesaian.setText("");
                    JTAsalInstitusi.requestFocus();
                }
            }
        }
    }

    void ubahData() {
        if (checkInput()) {
            Update update = new Update();
            Boolean berhasil = false;
            berhasil = update.Ubah("UPDATE `snitbpengaduanpelanggan` SET `Tanggal`='" + FDateF.datetostr(JDTanggalPengaduan.getDate(), "yyyy-MM-dd") + "',`AsalInstitusi`='" + JTAsalInstitusi.getText() + "',`YangMenerima`='" + JTYangMenerima.getText() + "',`JenisPengaduan`='" + JCJenisPengaduan.getSelectedItem() + "',`BagianYangMenangani`='" + JCBagianYangMenangani.getSelectedItem() + "',`RincianPengaduan`='" + JTARincianPengaduan.getText() + "',`TindakLanjutPengaduan`='" + JTATindakLanjut.getText() + "',`TindakanPencegahan`='" + JTATindakanPencegahan.getText() + "',`TargetTanggalPenyelesaian`='" + FDateF.datetostr(JDTanggalTargetPenyelesaian.getDate(), "yyyy-MM-dd") + "',`VerifikasiPenyelesaian`='" + JTVerifikasiPenyelesaian.getText() + "',`Selesai`=" + JCBSelesai.isSelected() + " WHERE `IdPengaduan` = " + IdEdit, "Pengaduan Pelanggan", this);
            if (listPengaduanPelanggan != null) {
                listPengaduanPelanggan.load();
            }
            if (berhasil) {
                dispose();
            }
        }
    }

