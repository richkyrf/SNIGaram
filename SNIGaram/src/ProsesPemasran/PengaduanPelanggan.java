    void loadEditData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.setQuery("SELECT `IdPengaduan` as 'ID', DATE_FORMAT(`Tanggal`,'%d-%m-%Y') as 'Tanggal', `AsalInstitusi` as 'Asal/Institusi', `YangMenerima` as 'Yang Menerima', `JenisPengaduan` as 'Jenis Pengaduan', `BagianYangMenangani` as 'Bagian Yang Menangani', `RincianPengaduan` as 'Rincian Pengaduan', `TindakLanjutPengaduan` as 'Tindak Lanjut Pengaduan', `TindakanPencegahan` as 'Tindakan Pencegahan', DATE_FORMAT(`TargetTanggalPenyelesaian`,'%d-%m-%Y') as 'Target Tanggal Penyesuaian', `VerifikasiPenyelesaian` as 'Verifikasi Penyelesaian', IF(`Selesai`=1,'Selesai','Belum Selesai') as 'Status' FROM `snitbpengaduanpelanggan` WHERE `IdPengaduan` = " + IdEdit);
        ArrayList<String> list = dRunSelctOne.excute();
        JDTanggalPengaduan.setDate(FDateF.strtodate(list.get(1), "dd-MM-yyyy"));
        JTAsalInstitusi.setText(list.get(2));
        JTYangMenerima.setText(list.get(3));
        JCJenisPengaduan.setSelectedItem(list.get(4));
        JCBagianYangMenangani.setSelectedItem(list.get(5));
        JTARincianPengaduan.setText(list.get(6));
        JTATindakLanjut.setText(list.get(7));
        JTATindakanPencegahan.setText(list.get(8));
        JDTanggalTargetPenyelesaian.setDate(FDateF.strtodate(list.get(9), "dd-MM-yyyy"));
        JTVerifikasiPenyelesaian.setText(list.get(10));
        JCBSelesai.setSelected(list.get(11).equals("Selesai"));
    }

    Boolean checkInput() {
        if (JDTanggalPengaduan.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Tanggal Pengaduan Tidak Boleh Kosong");
            return false;
        } else if (JTAsalInstitusi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Asal / Institusi Tidak Boleh Kosong");
            return false;
        } else if (JTYangMenerima.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Yang Menerima Tidak Boleh Kosong");
            return false;
        } else if (JDTanggalTargetPenyelesaian.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Tanggal Target Penyelesaian Tidak Boleh Kosong");
            return false;
        } else {
            return true;
        }
    }

